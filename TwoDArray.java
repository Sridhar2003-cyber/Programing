public class TwoDArray {
    public static void main(String[] args) {
        int[][]marks = {{72,75,69},{59,74,58}};
        for(int i=0;i<2;i++)
        {
            for(int j=0;j<3;j++){
                
            System.out.print(marks[i][j]+" ");}
            System.out.println("\n");
        }
    }
}
